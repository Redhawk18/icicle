pub fn selection_time(interval: u64) -> Row<'static, Message> {
    row!(
        number_input(interval),
        column!(
            text("Unit:"),
            SelectionList::new(
                &[
                    Time::Minutes,
                    Time::Seconds,
                    Time::Milliseconds,
                    Time::Mircoseconds,
                    Time::Nanoseconds,
                ],
                Message::Unit,
            )
            .height(Length::Fixed(120.0))
            .width(Length::Fixed(100.0))
        )
    )
    .align_items(Alignment::Center)
    .spacing(10.0)
}
